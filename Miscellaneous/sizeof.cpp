#include <bits/stdc++.h>

using namespace std;

int main(void) {
	printf("sizeof(char) = %ld\n", sizeof(char));
	printf("sizeof(short) = %ld\n", sizeof(short));
	printf("sizeof(int) = %zu\n", sizeof(int));
	printf("sizeof(long) = %zu\n", sizeof(long));
	printf("sizeof(long long) = %ld\n", sizeof(long long));
	return 0;
}